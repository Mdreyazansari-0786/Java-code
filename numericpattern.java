public class numericpattern {
    public static void main(String[] args) {
        for(int i=1;i<=4;i++){
            for(int j=1;j<=i;j++){
                //if you change j to i than the pattern was 1 22 333 4444.
                System.out.print(j);
            }
            System.out.println();
        }
        

    }
}
